@pytest.mark.asyncio
@pytest.mark.parametrize("case", TEST_CASES, ids=[c["name"] for c in TEST_CASES])
async def test_matching_full_pipeline(matcher_service_real, case):
    """
    Интеграционный тест полного пайплайна:
    Векторы -> Атрибуты -> Реальный LLM (если нужно) -> Результат
    """
    req_data = case["request"]
    expected = case["expected"]
    
    # Создаем запрос
    request = MatchRequest(**req_data)
    
    # Выполняем с таймаутом (защита от зависания LLM)
    try:
        result = await asyncio.wait_for(
            matcher_service_real.process(request), 
            timeout=120  # 2 минуты на сложный кейс с генерацией
        )
    except asyncio.TimeoutError:
        pytest.fail(f"Test timed out for case: {case['name']}")

    # --- Логирование для отладки ---
    print(f"\n🧪 TEST: {case['name']}")
    print(f"🎯 EXPECTED: {expected}")
    print(f"✅ RESULT: {result.best_match}")
    print(f"📡 SOURCE: {result.source}")
    print(f"📊 SCORE: {result.confidence}")
    if result.source == "llm_fallback":
        print(f"🤖 REASONING: {result.reasoning}")

    # --- Ассерты ---
    
    # 1. Если ожидаем отсутствие матча
    if expected is None:
        assert result.best_match is None, f"❌ Ожидалось None, но вернулось: {result.best_match}"
        # Источник может быть no_match (векторы отклонили) или llm_fallback (LLM отклонил)
        assert result.source in ["no_match", "llm_fallback"], f"❌ Неожиданный source: {result.source}"
    
    # 2. Если ожидаем конкретный матч
    else:
        # Проверяем, что результат совпадает или является подстрокой (защита от пунктуации)
        # Важно: result.best_match — это точная строка из competitor_names
        match_found = (
            result.best_match == expected or 
            expected in result.best_match or 
            result.best_match in expected
        )
        
        if not match_found:
            # Выводим доступные варианты для анализа
            available = req_data["competitor_names"]
            print(f"⚠️ Available options: {available}")
            
        assert match_found, f"❌ Матч не найден. Ожидалось: '{expected}', получено: '{result.best_match}'"
        
        # Если матч найден, source не должен быть "no_match"
        assert result.source != "no_match", "❌ Матч найден, но источник 'no_match'"
